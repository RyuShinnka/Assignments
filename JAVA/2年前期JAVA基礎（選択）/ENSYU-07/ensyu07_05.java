public class ensyu07_05 {

    // 文字列を受け取って表示するメソッド
    public static void printMessage(String message) {
        System.out.println(message);
    }

    public static void main(String[] args) {
        // 3つの文字列を順に表示
        printMessage("こんにちは");
        printMessage("元気ですか");
        printMessage("また会いましょう");
    }
}
