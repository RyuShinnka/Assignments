public class ensyu01_01 {
    public static void main(String[] args) {
        // 数値を設定
        int num1 = 82;
        int num2 = 17;
        // 和と差を計算
        int sum = num1 + num2;
        int sa = num1 - num2;
        // 結果を表示
        System.out.println("82 と 17 の和:" + sum);
        System.out.println("82 と 17 の差:" + sa);
    }
}
