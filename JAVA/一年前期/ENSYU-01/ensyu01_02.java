public class ensyu01_02 {
    public static void main(String[] args) {
        // 数値を設定
        int X = 82;
        int Y = 17;
        int Z = 39;
        // 合計と平均を計算
        int sum = X + Y + Z;
        int avg = sum / 3;
        // 結果を表示
        System.out.println("X と Y と Zの合計:" + sum);
        System.out.println("X と Y と Zの平均:" + avg);
    }
}
