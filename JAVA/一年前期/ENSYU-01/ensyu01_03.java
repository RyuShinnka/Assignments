public class ensyu01_03 {
    public static void main(String[] args) {
        // 数値を設定
        double A = 0.39;
        double B = 39.9;
        // 合計と平均を計算
        double sum = A + B;
        double avg = sum / 2;
        // 結果を表示
        System.out.println("変数A と B の合計:" + sum);
        System.out.println("変数A と B の平均:" + avg);
    }
}
