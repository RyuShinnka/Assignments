public class ensyu07_5 {
    // 文字列を表示するメソッド
    public static void display(String s) {
        System.out.println(s);
    }

    public static void main(String args[]) {
        // 各文字列を表示
        String s1 = "こんにちは";
        display(s1);
        String s2 = "お元気ですか";
        display(s2);
        String s3 = "また会いましょう";
        display(s3);
    }
}
