<!-- 1 20 回ループして数字を1から20まで、1つずつ改行して表示する
for ループを使用する
2 ただし、3の倍数(3で割った余りが0なら3の倍数)の時だけ
「あほ」と表示して、1回ループから抜ける(continue)(数字は表示しない)
3 さらに、あほが5回以上になったらループから抜けて終了する(break)
4 さらに、8 の倍数の時だけ「ワン」と表示して
1回ループから抜ける(continue)(数字は表示しない)
5 最後におもろーと表示する -->

<?php
$ahoCount = 0; // あほの回数をカウントする変数

for ($i = 1; $i <= 20; $i++) {
    if ($i % 3 == 0) {
        echo "あほ<br>";
        $ahoCount++;
        
        // あほが5回以上になったらループ終了
        if ($ahoCount >= 5) {
            break;
        }
        
        continue;
    }
    
    if ($i % 8 == 0) {
        echo "ワン<br>";
        continue;
    }
    
    echo $i . "<br>"; // 通常の数字を表示
}

echo "おもろー";
?>
