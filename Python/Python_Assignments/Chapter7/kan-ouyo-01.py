# 関数の処理	
# 	引数の変数をｓｗとkazuにする
# 	引数のSWが1の時は下記のように表示をする
# 	表示する内容は、引数のkazuの値を下記のように表示する
# 	「計算結果は、〇〇〇です。」
	
# 	ｓｗの値が1でなければ「1万円未満でした。」と表示する
	
# メイン処理	
# 	キーボードから商品の単価と数量を入力
# 	金額を計算し、1万円以上の時は関数で表示する
# 	関数に表示すための値と金額を引き渡す
# 	金額の計算は単価×数量


# 関数の処理: swの値に応じてメッセージを表示
def calc(sw, kazu):
    if sw == 1:  # 1万円未満の場合
        print("1万円未満でした。")
    else:  # 1万円以上の場合
        print("計算結果は、", kazu, "です。")

# メイン処理
# 商品の単価と数量を入力
price = int(input("商品の単価を入力->"))
qua = int(input("商品の数量を入力->"))

# 合計金額を計算
total = price * qua

# 合計が1万円未満かどうかを判定して関数を呼び出す
if total < 10000:
    calc(1, total)  # 1万円未満の場合
else:
    calc(0, total)  # 1万円以上の場合
