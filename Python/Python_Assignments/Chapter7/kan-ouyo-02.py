# 関数の処理		引数有：戻り値有
# 	TBL＝［20,60,50,10,70,30,90,100,85,65]を定義する	
# 	引数でキーを受け取り検索する	
# 	キーの値と同じ値があった場合は戻り値に１とその番地を返す	
# 	キーの値と同じ値が無いときは２の値と番地99を返す	
		
# メイン処理		
# 	キーボードから検索するキーとなる値を入力	
# 	関数にキーの値を引き渡して検索をする	
# 	検索結果を判定して下記のように表示する	
# 	戻り値の番地が99でない時は「〇〇番地に見つかりました。」	
# 	戻り値の番地が99の時は「TBLに見つかりませんでした。」	
		
		
# 関数の処理: 指定されたキーがTBLにあるか検索する
def search(key):
    TBL = [20, 60, 50, 10, 70, 30, 90, 100, 85, 65]  # 検索対象のリスト
    for i in range(len(TBL)):  # TBLの要素を順にチェック
        if TBL[i] == key:  # キーと一致する値が見つかった場合
            return 1, i  # ステータス1とその番地を返す
    return 2, 99  # 見つからなかった場合はステータス2と99を返す

# メイン処理
key = int(input("検索するキーを入力->"))  # 検索するキーを入力
result = search(key)  # 関数を呼び出して結果を取得

# 結果の判定と表示
if result[1] != 99:  # インデックスが99でない場合は見つかったと判断
    print(result[1], "番地に見つかりました。")
else:
    print("TBLに見つかりませんでした。")
