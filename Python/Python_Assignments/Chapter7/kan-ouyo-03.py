# 関数の処理	
# 	引数：点数１＝０　点数２＝０
	
# 	点数１と点数２の合計を計算すし、戻り値とする
# 	下記の判定をして、文章を戻り値とする。
# 	点数１と点数２がどちらも６０点以上の時は、「合格」
# 	どちらかが60点以上の時は、「もう少し頑張りましょう」
# 	どちらも６０点未満の時は、「不合格」
	
# メイン処理	
# 	テストの点数１と点数２と名前をキーボードから入力
# 	関数を呼び出して、結果を表示する
# 	表示は下記のようにする。
# 	「〇〇さんは、合計点が〇〇〇点です。評価は〇〇〇です。」
	
# 関数の処理: 2つの点数を受け取り、合計点と評価を返す
def calc(point1=0, point2=0):
    sum = point1 + point2  # 点数の合計を計算
    if point1 >= 60 and point2 >= 60:  # 両方の点数が60点以上
        return sum, "合格"
    elif point1 >= 60 or point2 >= 60:  # どちらか一方が60点以上
        return sum, "もう少し頑張りましょう"
    else:  # 両方とも60点未満
        return sum, "不合格"

# メイン処理
# 名前と2つのテストの点数を入力
name = input("名前を入力->")
point1 = int(input("テストの点数１を入力->"))
point2 = int(input("テストの点数２を入力->"))

# 関数を呼び出して合計点と評価を取得
sum, result = calc(point1, point2)

# 結果を出力
print(name + "さんは、合計点が" + str(sum) + "点です。評価は" + result + "です。")

	
	
