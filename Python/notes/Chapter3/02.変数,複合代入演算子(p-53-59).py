# 書式；変数の代入方法
# 変数名　＝　データの値
# 例：x = 10
num1 = 5
num2 = 3
result1 = num1 + num2
result2 = num1 - num2
result3 = num1 * num2
print(result1)  # 8
print(result2)  # 2
print(result3)  # 15

# 代入を複数回行うことも可能

num1 = 5
print(num1)  # 5
num1 = 10
print(num1)  # 10

# 注意！　代入を行っていない変数は、エラーが発生します。

